/*QUESTION = write a program print array by for each loop*/

import java.util.*;
import java.io.*;
public class Main
{
	public static void main(String[] args) {
	    // initialization of array
	   int[] a =  {20,40,50,60};
	        // for each loop
	    for (int i:a){
	        System.out.print(i+" ");
	    }
	   
	 }
	 
	 
}