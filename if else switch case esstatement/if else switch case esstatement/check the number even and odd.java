/*check the number are odd or even*/
import java.util.*;
import java.io.*;
public class Main
{
	public static void main(String[] args) {
		Scanner sc = new Scanner(System.in);
	       int num = sc.nextInt();
	
		
		if (num%2==0){
		    System.out.print(num+" ="+"number is even");
		}else{
		    System.out.print(num+" = "+"number is odd ");
		}
		}
}


